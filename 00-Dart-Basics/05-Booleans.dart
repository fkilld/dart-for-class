void main() {
  // Declaring a boolean variable with the value 'true'
  var theTruth = true; // 'theTruth' is a boolean representing a true value

  // Declaring another boolean variable with the value 'false'
  var theFalse = false; // 'theFalse' is a boolean representing a false value

  // Printing the value of the boolean variable 'theTruth'
  print(theTruth); // Output: true

  // Printing the value of the boolean variable 'theFalse'
  print(theFalse); // Output: false
}
